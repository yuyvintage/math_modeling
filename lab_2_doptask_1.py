print('ax**2 + bx + c = 0')

a = int(input('введите первый коэффицент а:'))
b = int(input('введите второй коэффицент b:'))
c = int(input('введите третий коэффицент c:'))

d = (b**2) - (4 * a * c)

x1 = (-b+(d**0.5))/(2 * a)
x2 = (-b-(d**0.5))/(2 * a)
if d == 0:
    print('x=', x1)
elif d > 0:
    print('x1=', x1, 'x2=', x2)
else:
    print('нет корней')